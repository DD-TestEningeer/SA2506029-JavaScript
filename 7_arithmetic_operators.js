


// Arithmetic Operators 

// 1) Addition 

let num1 =100;
let num2 = 200;

let result = 0;

result = num1 + num2;

console.log("The sum of num1 and num2 : " + result);


// 2) Subtraction 

let num3 =100;
let num4 = 200;

let result2 = 0;

result2 = num3 - num4;

console.log("The subtraction of num3 and num4 : " + result2);


// 3) Multiply 

let num5 =100;
let num6 = 200;

let result3 = 0;

result3 = num5 * num6;

console.log("The multiplication of num5 and num6 : " + result3);


// 4) Divide 

let num7 =100;
let num8 = 200;

let result4 = 0;

result4 = num7/num8;

console.log("The division of num7 and num8 : " + result4);



// 5) Remainder 

let num9 =11;
let num10 = 5;

let result5 = 0;

result5 = num9%num10;

console.log("The remainder of num7 and num8 : " + result5);

// 6) Increment and Decrement

let testNumber = 11;
testNumber++; // post increment
console.log("The increment operator use : " + testNumber);


let testNumber2 = 22;
// ++testNumber; // pre increment
console.log("The pre-increment operator use : " + (++testNumber2));



let testNumber3 = 11;
testNumber3--; // post decrement
console.log("The decrement operator use : " + testNumber3);


let testNumber4 = 22;
// --testNumber4; // pre decrement
console.log("The pre-decrement operator use : " + (--testNumber4));
