

// Switch Case statements 


let days = "ABC";

switch(days){

case "Mon" : {

     console.log("Monday");
     break;
} 


case "Tue" : {

     console.log("Tuesday");
     break;
} 

case "Wed" : {

     console.log("Wednesday");
     break;
} 


default :

console.log("No case is matching : please try to select correct choice");




}