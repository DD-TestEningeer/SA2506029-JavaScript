

// Implicit Type Conversion - Automatically

let username = "DemoUser"; // String

let testCaseCount = 10; // Number


let result = username + testCaseCount;

console.log(result);
console.log(typeof(result));


// Explicit Conversion 


let numberString = "123"; // string
console.log(typeof(numberString));

let number = Number(numberString); // number
console.log(typeof(number));



